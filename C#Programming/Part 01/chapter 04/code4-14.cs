//for반복문으로 곱셈

using System;

namespace forloop
{
    class forloops
    {
        static void Main  (string[] args)
        {
            
            //반보ㅓㄱ문수행
            for (int i = '가' ; i <= '힣' ; i++)
            {//출력
                Console.Write((char)i);
            }

            
            
        }
    }
    
}